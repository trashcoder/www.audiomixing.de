    <h3><a href="?article=0004">FolderSync – Thunderbird Add-on zum Synchronisieren von Ordnern</a></h3>
    <p>FolderSync ist ein Add-on für <strong>Mozilla Thunderbird</strong>, das das automatische Synchronisieren von
    E-Mail-Ordnern zwischen verschiedenen Konten ermöglicht. Wer mehrere E-Mail-Konten verwaltet und Nachrichten
    strukturiert an einem Ort haben möchte, kann mit FolderSync Ordner regelmäßig abgleichen – ohne manuelles
    Verschieben oder Kopieren von E-Mails.</p>
    <p>Das Add-on steht kostenlos im offiziellen <strong>Thunderbird Add-on-Verzeichnis</strong> zur Verfügung und
    lässt sich direkt über den Add-on-Manager von Thunderbird installieren.</p>
