    <h3><a href="?article=0003">Real Panning – ein VST3-Plugin für realistisches Stereo-Panning</a></h3>
    <p>Real Panning ist ein natives C++-VST3-Plugin, das räumliche Klangortung physiologisch korrekt simuliert. Im Gegensatz
    zu einfachem Amplituden-Panning kombiniert es zwei akustische Wahrnehmungsmechanismen: den <strong>ILD</strong>
    (Interaural Level Difference), bei dem das weiter entfernte Ohr weniger Lautstärke empfängt (Equal-Power-Gesetz),
    und den <strong>ITD</strong> (Interaural Time Difference), bei dem der Schall das weiter entfernte Ohr mit einer
    kleinen Verzögerung erreicht (Woodworth-Formel). Das Ergebnis ist ein deutlich natürlicheres und räumlicheres
    Klangbild als bei herkömmlichem Panning.</p>
